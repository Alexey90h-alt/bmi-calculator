# BMI Calculator - Калькулятор Индекса Массы Тела

print("--- Добро пожаловать в калькулятор ИМТ ---")

# Запрашиваем данные. float позволяет вводить дробные числа через точку.
# weight — вес, height — рост
weight = float(input("Введите ваш вес в кг (например, 75.5): "))
height = float(input("Введите ваш рост в метрах (например, 1.75): "))

# Считаем ИМТ по формуле: вес разделить на квадрат роста
# bmi — распространенная аббревиатура Body Mass Index
bmi = weight / (height ** 2)

# Округляем результат до одного знака после запятой для красоты
bmi = round(bmi, 1)

print(f"Ваш индекс массы тела: {bmi}")

# Проверяем результат и выводим пояснение 
if bmi < 18.5:
    print("Результат: Недостаточный вес. Пора перекусить! 🍎")
elif 18.5 <= bmi <= 24.9:
    print("Результат: Нормальный вес. Вы в отличной форме! ✅")
elif 25 <= bmi <= 29.9:
    print("Результат: Избыточный вес. Стоит добавить активности. 🥗")
else:
    print("Результат: Ожирение. Берегите здоровье! 💪")
print()
print("Как алгоритм — расчет верен, сделан по формуле Кетле.\nНо есть нюансы, такие, как:\n- Мышцы тежелее жира\n- Тип телосложения\n- Возраст")
print()